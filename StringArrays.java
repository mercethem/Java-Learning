public class StringArrays {
    public static void main(String[] args) {
        String arr[] = {"java", "python", "pascal", "smalltalk", "ada", "javascript"};
        java.util.Arrays.sort(arr);
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i] + " ");

        }


    }
}
